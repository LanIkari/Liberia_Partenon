    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int fila = this.jTable2.getSelectedRow();
        if(fila!=-1){
        ((TablaModelLibro)this.jTable2.getModel()).getDatos().remove(fila);
        ((TablaModelLibro)this.jTable2.getModel()).fireTableDataChanged();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecciona una fila");
        }
    }//GEN-LAST:event_jButton4ActionPerformed


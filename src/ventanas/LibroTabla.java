    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int fila = this.jTable2.getSelectedRow();
        if(fila!=-1){
            Libro dialog = new Libro(new javax.swing.JFrame(), true);
                dialog.addWindowListener(new java.awt.event.WindowAdapter() {
                    @Override
                    public void windowClosing(java.awt.event.WindowEvent e) {
                        dispose();
                    }
                });
                
                TablaModelLibro tmp = (TablaModelLibro)this.jTable2.getModel();
                dialog.setDatos(tmp.getDatos());
                dialog.setModificar(true);
                dialog.setFila(this.jTable2.getSelectedRow());
                dialog.setTabla(this.jTable2);
                dialog.modificar();
                dialog.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecciona una fila");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

